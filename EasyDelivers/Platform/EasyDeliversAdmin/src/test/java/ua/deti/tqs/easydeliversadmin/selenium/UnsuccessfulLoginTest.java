package ua.deti.tqs.easydeliversadmin.selenium;// Generated by Selenium IDE

import io.github.bonigarcia.seljup.SeleniumJupiter;
import org.junit.jupiter.api.AfterEach;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.openqa.selenium.By;
import org.openqa.selenium.WebDriver;
import org.openqa.selenium.firefox.FirefoxDriver;
import org.openqa.selenium.Dimension;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.JavascriptExecutor;
import org.openqa.selenium.Keys;
import java.util.*;

@ExtendWith(SeleniumJupiter.class)
public class UnsuccessfulLoginTest {
  private WebDriver driver;
  private Map<String, Object> vars;
  JavascriptExecutor js;

  @BeforeEach
  public void setUp() {
    System.setProperty("webdriver.gecko.driver", "src/test/resources/geckodriver");
    driver = new FirefoxDriver();
    js = (JavascriptExecutor) driver;
    vars = new HashMap<String, Object>();
  }

  @Test
  public void unsuccessfullogin() {
    driver.get("http://localhost:8080/login");
    driver.manage().window().setSize(new Dimension(783, 691));
    driver.findElement(By.name("email")).click();
    driver.findElement(By.name("email")).sendKeys("wrongmail@ua.pt");
    driver.findElement(By.name("password")).sendKeys("wrongpass");
    driver.findElement(By.name("password")).sendKeys(Keys.ENTER);
    driver.findElement(By.cssSelector("h1")).click();
    {
      List<WebElement> elements = driver.findElements(By.cssSelector("h1"));
      assert(elements.size() > 0);
    }
  }

  @AfterEach
  public void tearDown() {
    driver.quit();
  }

}
